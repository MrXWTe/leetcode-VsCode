class Solution {
    public int titleToNumber2(String s) {
        int res = 0;
        char[] chars = s.toCharArray();
        int len = chars.length;
        for(int i = 0; i<len; i++){
            int bit = len - i - 1;
            res += (chars[i] - 'A' + 1) * Math.pow(26, bit);
        }
        return res;
    }

    public int titleToNumber(String s) {
        int ans = 0;
        for(int i=0;i<s.length();i++) {
            int num = s.charAt(i) - 'A' + 1;
            ans = ans * 26 + num;
        }
        return ans;
    }
}

